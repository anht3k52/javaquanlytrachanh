    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            Ketnoi a = new Ketnoi();
            Connection conn = a.getConnection();
            PreparedStatement ps = conn.prepareStatement("INSERT INTO Nhanvien (MaNV, TenNV, Gioitinh, Diachi, SDT, Chucvu, Ngaysinh) VALUES (?,?,?,?,?,?,?)");
            ps.setString(1, txtmanv.getText());
            ps.setString(2, txttennv.getText());
            if(rnam.isSelected())
            {
                ps.setBoolean(3, true);
            }
            else
            {
                ps.setBoolean(3, false);
            }
            ps.setString(4, txtdiachi.getText());
            ps.setString(5, txtsdt.getText());
            ps.setString(6, txtchucvu.getText());
            ps.setString(7, txtngaysinh.getText());
            int chx = ps.executeUpdate();
            if(chx>0)
            {
                JOptionPane.showMessageDialog(this,"them thanh cong");
                tbn.setRowCount(0);
                loaddata(); //trc khi load data set do cot ve 0 neu khong no se load du lieu len bang them 1 lan nua
            }
            
        }
        catch(Exception ex){
              System.out.println(ex.toString());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

