    public void loaddata()
    {
        try{
            Ketnoi a = new Ketnoi();
            Connection conn = a.getConnection();
            int number;
            Vector row,column ;
            column = new Vector();
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM Nhanvien");
            ResultSetMetaData metadata = rs.getMetaData();
            number = metadata.getColumnCount(); //tra ve so cot
            for(int i = 1;i <= number;i++)
            {
                column.add(metadata.getColumnName(i));//lay ra tieu de cac cot
            }
            tbn.setColumnIdentifiers(column);;
            while(rs.next()){
                row = new Vector();
                for(int i=1;i<=number;i++)
                {
                    row.addElement(rs.getString(i));
                }
                tbn.addRow(row);
                tb1.setModel(tbn);
            }
            
            //load du lieu tu bang len text field
            tb1.getSelectionModel().addListSelectionListener( new ListSelectionListener() {
                @Override
                public void valueChanged(ListSelectionEvent e) {
                    if(tb1.getSelectedRow() >= 0)
                    {
                        txtmanv.setText(tb1.getValueAt(tb1.getSelectedRow(), 0)+"");
                         txttennv.setText(tb1.getValueAt(tb1.getSelectedRow(), 1)+"");
                           txtdiachi.setText(tb1.getValueAt(tb1.getSelectedRow(), 3)+"");
                            txtsdt.setText(tb1.getValueAt(tb1.getSelectedRow(), 4)+"");
                             txtchucvu.setText(tb1.getValueAt(tb1.getSelectedRow(), 5)+"");
                             txtngaysinh.setText(tb1.getValueAt(tb1.getSelectedRow(), 6)+"");
                    }
                }
            });
        }
        catch(Exception ex)
        {
            System.out.println(ex.toString());
        }
    }

