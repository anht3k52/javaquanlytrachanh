        try{
            Ketnoi a = new Ketnoi();
            Connection conn = a.getConnection();
            PreparedStatement ps = conn.prepareStatement("INSERT INTO Baocao (MaBC, TenBC, Mahoadon, Thoigian, Doanhthu) VALUES (?,?,?,?,?)");
            ps.setString(1, txtMabc.getText());
            ps.setString(2, txtTenbc.getText());
            ps.setString(3, txtMahd.getText());
            ps.setString(4, txtTg.getText());
            ps.setString(5, txtDoanhthu.getText());
            int chx = ps.executeUpdate();
            if(chx>0)
            {
                JOptionPane.showMessageDialog(this,"them thanh cong");
                tbn.setRowCount(0);
                loaddata(); //trc khi load data set cai cot ve 0 neu khong no se load du lieu len bang them 1 lan nua
            }
            
        }
        catch(Exception ex){
              System.out.println(ex.toString());
        }
    
