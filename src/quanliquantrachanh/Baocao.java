    public void loaddata()
    {
        try{
            Ketnoi a = new Ketnoi();
            Connection conn = a.getConnection();
            int number;
            Vector row,column ;
            column = new Vector();
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM Baocao");
            ResultSetMetaData metadata = rs.getMetaData();
            number = metadata.getColumnCount(); //tra ve so cot
            for(int i = 1;i <= number;i++)
            {
                column.add(metadata.getColumnName(i));//lay ra tieu de cac cot
            }
            tbn.setColumnIdentifiers(column);;
            while(rs.next()){
                row = new Vector();
                for(int i=1;i<=number;i++)
                {
                    row.addElement(rs.getString(i));
                }
                tbn.addRow(row);
                baocao.setModel(tbn);
            }
            
            //load du lieu tu bang len text field
            baocao.getSelectionModel().addListSelectionListener( new ListSelectionListener() {
                @Override
                public void valueChanged(ListSelectionEvent e) {
                    if(baocao.getSelectedRow() >= 0)
                    {
                        txtMabc.setText(baocao.getValueAt(baocao.getSelectedRow(), 0)+"");
                        txtTenbc.setText(baocao.getValueAt(baocao.getSelectedRow(), 1)+"");
                        txtMahd.setText(baocao.getValueAt(baocao.getSelectedRow(), 2)+"");
                        txtTg.setText(baocao.getValueAt(baocao.getSelectedRow(), 3)+"");
                        txtDoanhthu.setText(baocao.getValueAt(baocao.getSelectedRow(), 4)+"");
                    }
                }
            });
        }
        catch(Exception ex)
        {
            System.out.println(ex.toString());
        }
    }

